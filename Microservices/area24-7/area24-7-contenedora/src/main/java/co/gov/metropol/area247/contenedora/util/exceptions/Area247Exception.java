package co.gov.metropol.area247.contenedora.util.exceptions;

public class Area247Exception extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2851527130785685410L;

	public Area247Exception() {
		super();
	}

	public Area247Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public Area247Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public Area247Exception(String message) {
		super(message);
	}

	public Area247Exception(Throwable cause) {
		super(cause);
	}
	
	

}
