package co.gov.metropol.area247.services.rest.metro;

public class GeometryCivicaExpedicion {
	
	private float x;
	private float y;
	
	public GeometryCivicaExpedicion(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
	
}
